const val =3
switch(val){
    case 1:
    console.log("jan");
    break;
    case 2:
    console.log("feb");
    break;
    case 3:
    console.log("march");
    case 4:
    console.log("April");

    default:
         console.log("default case match");
         break; 
}
// output:- 
//  march
//  April
